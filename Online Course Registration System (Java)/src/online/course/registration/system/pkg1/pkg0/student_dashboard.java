    public void addPopup(){
        JMenuItem show = new JMenuItem("Show details");
        JMenuItem drop = new JMenuItem("Drop course");
        
        //Add items to Popup Menu
        pop.add(show);
        pop.add(drop);
        
        //Events
        show.addActionListener(new ActionListener(){
            @Override
            public void actionPerformed(ActionEvent e ){
                c1.generateDetails(courseComboBox.getSelectedItem().toString(),"listBox");
            }
        });
        
        drop.addActionListener(new ActionListener(){
            @Override
            public void actionPerformed(ActionEvent e ){
                for (String s : s_info)
                {
                    if(reg_course_list.getSelectedValue().equals(s)){
                        s_info.remove(s);
                        break;
                    }
                }
                EditFile f = new EditFile();
                f.changeALineInATextFile("Student.txt",f.arrayListToString(s_info),s_info.get(0));
                student_dashboard.this.hide();
                student_dashboard f1 = new student_dashboard(s_info);
                f1.pack();
                f1.setSize(597, 610);
                f1.setLocationRelativeTo(null);
                f1.setVisible(true);
                f1.getContentPane().requestFocusInWindow();
            }
        });
    }

