    private void reg_course_listMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_reg_course_listMouseClicked
        //This code is for adjusting the position of the options menu when right click is done by user.

        if(SwingUtilities.isRightMouseButton(evt) && reg_course_list.locationToIndex(evt.getPoint()) == reg_course_list.getSelectedIndex()){
            //Make sure sth is selected
            if( ! reg_course_list.isSelectionEmpty()){
                pop.show(reg_course_list,evt.getX(),evt.getY());
            }
        }
    }//GEN-LAST:event_reg_course_listMouseClicked

